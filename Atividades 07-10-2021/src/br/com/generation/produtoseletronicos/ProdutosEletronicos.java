package br.com.generation.produtoseletronicos;

public class ProdutosEletronicos {



public String getTipoDeAparelho() {
		return tipoDeAparelho;
	}
	public void setTipoDeAparelho(String tipoDeAparelho) {
		this.tipoDeAparelho = tipoDeAparelho;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public double getPreco() {
		return preco;
	}
	public void setPreco(double preco) {
		this.preco = preco;
	}
private String tipoDeAparelho;
private String marca;
private String modelo;
private double preco;

}
